function consultarSd() {
  
  const inputConsulta = document.getElementById('consul').value
  let resultados = []

  for (const rua in estoque) {

      const encontrados = estoque[rua].filter(item => item.sd == inputConsulta);
      resultados = resultados.concat(encontrados); 

  }

  if (resultados.length > 0) {

      let mensagem = `SD: ${inputConsulta} <br>Rua: ${resultados[0].rua} <br>Nível: ${resultados[0].nivel} <br>Blocos: `;
      mensagem += resultados.map(item => item.bloco).join(", ");

      document.getElementById("mensagemTexto").innerHTML = mensagem;
      document.getElementById("overlay").style.display = "block";
      document.getElementById("mensagemModal").style.display = "flex";

  } else {

      document.getElementById("mensagemTexto").innerHTML = "Item não encontrado.";
      document.getElementById("overlay").style.display = "block";
      document.getElementById("mensagemModal").style.display = "flex";
      
  }


}

